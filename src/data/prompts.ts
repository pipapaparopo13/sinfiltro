// Funci√≥n para obtener prompts de una biblioteca personalizada o los predeterminados
export async function getPromptsForGame(
    count: number,
    customLibraryCode?: string,
    gameMode?: string
): Promise<string[]> {

    // üå∂Ô∏è Si el modo es picante, usar prompts picantes
    if (gameMode === 'spicy') {
        const { getRandomSpicyPrompts } = await import('./spicyPrompts');
        console.log('üå∂Ô∏è Using SPICY prompts for adult mode!');
        return getRandomSpicyPrompts(count);
    }

    if (customLibraryCode) {
        try {
            // Import dinamically to avoid circular dependency
            const { getLibrary, incrementPlayCount } = await import('@/lib/customPrompts');
            const library = await getLibrary(customLibraryCode);

            if (library && library.prompts && library.prompts.length > 0) {
                // Increment play count
                await incrementPlayCount(customLibraryCode);

                // Shuffle custom prompts
                const shuffled = [...library.prompts].sort(() => Math.random() - 0.5);

                // If not enough custom prompts, fill with default ones
                if (shuffled.length < count) {
                    const remainingCount = count - shuffled.length;
                    const defaultPrompts = getRandomPrompts(remainingCount);
                    return [...shuffled, ...defaultPrompts];
                }

                return shuffled.slice(0, count);
            }
        } catch (error) {
            console.error('Error loading custom library:', error);
        }
    }

    // Fallback to default prompts
    return getRandomPrompts(count);
}

